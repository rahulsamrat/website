<h3> About SILIKA </h3>
		<hr>
        <p>The journey of <strong>SILIKA</strong> started with installation of GC-MS-MS and ASE 350 in 2012. Subsequently
		, MAT 253 was installed along with Gas Bench II, KIEL carbonate IV, GC-TC coupled with Isolink and Elemental
		Analyser 2000.<br>
		<strong>SILIKA</strong> offers analysis for stable isotopes of H, C, O, N and S in a wide variety of samples.
		Most samples can be efficiently analyzed in continuous flow mode in MAT 253. Dual inlet mode is used for very
		high precision work and for measuring isotopic ratios in sample processed off-line. For oragnic samples,
		before measuring the isotopic ratios, SILIKA has facility of characterising organic componds by GC-MS-MS,GC-FID
		and Pyrolyser.</p>
